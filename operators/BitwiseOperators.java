package operators;

public class BitwiseOperators {

	public static void main(String[] args) {
		
//		Bitwise Operators:
//			   - `&` (Bitwise AND)
//			   - `|` (Bitwise OR)
//			   - `^` (Bitwise XOR)
//			   - `~` (Bitwise NOT)
//			   - `<<` (Left shift)
//			   - `>>` (Right shift)
//			   - `>>>` (Unsigned right shift)
		
		int a = 5;
        int b = 7;
 
        // bitwise and
        // 0101 & 0111=0101 = 5
        System.out.println("a&b = " + (a & b));
		
	}

}
