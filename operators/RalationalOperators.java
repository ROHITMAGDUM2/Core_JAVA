package operators;

public class RalationalOperators {

	public static void main(String[] args) {
		/*
		 * == ==> same== equal
		 * !=  Not-equal to
		 * a < b  less===
		 * a > b  more==== 
		 * a <= b   equal to or less
		 * a >= b   equal to or greater
		 */
		
		
		int a= 10, b=20;
		
		System.out.println(a==b);
		

		System.out.println(a < b);
		
		System.out.println(a <= b);
		
		System.out.println(a != b);
		
		System.out.println(a >= b);
		
		System.out.println(a > b);
	}

}
