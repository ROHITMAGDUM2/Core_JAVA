package OOPS.keywords.finalkeyword;

/**
 * 
 * @author RM
 * 
 * Final Variable:
 *  if we create any final variable it become constant.
 *  we can not change the value of final variable
 *
 */
public class _01_FinalVariable {

	public static void main(String[] args) {
		final int a=10;
		
//		a = a + 20; // The final local variable a cannot be assigned. 
//		It must be blank and not using a compound assignment
		
		System.out.println(a);
	}

}
