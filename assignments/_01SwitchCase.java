package assignments;

 

public class _01SwitchCase{

 	public static void main(String[] args) {

     	String fruit = "mango";

        switch (fruit) {

         	default:

                System.out.print("ANY FRUIT WILL DO");

            case "Apple":

                System.out.print(" APPLE");

             case "Mango":

                System.out.print(" MANGO");

            case "Banana":

             	System.out.print(" BANANA");

                break;

        }

    }


}