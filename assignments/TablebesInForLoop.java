package assignments;
//Multiplication Table: Develop a program that takes an integer as input  and prints the multiplication table for that number up to 10 using a `for`  loop. 

public class TablebesInForLoop {

	public static void main(String[] args) {

	int	num1 = 5;
	
	for (int i= 1; i<=10; i++){
		System.out.println(num1+ " x "+ i +" = " + num1*i);
	}
		
	}

}
